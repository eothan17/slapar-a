function soma(num1, num2){
    return num1 + num2;
}
soma(22, 3);
console.log(soma(22, 3));

function quadrado(num3){
    return num3 * num3;
}
quadrado(20);
console.log(quadrado(20));

function quadrado2(num4){
    return num4**2;
}
quadrado2(10);
console.log(quadrado(10));

